package operators;


// < , > , <=, >=
public class RelationalOperator {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        boolean p=true;
        boolean q=false;
        System.out.println(a>b);
        System.out.println(b>a);
        System.out.println(a>=b);
        System.out.println(b<=a);
        System.out.println(p==q);
        System.out.println(!p);
    }
}
